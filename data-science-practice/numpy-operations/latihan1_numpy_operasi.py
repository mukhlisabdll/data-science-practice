# Latihan 8: Menggunakan NumPy untuk Operasi Matematika

# Mengimpor pustaka NumPy untuk operasi array numerik
import numpy as np

# Membuat array numpy
a = np.array([1, 2, 3, 4, 5]) # Membuat array a dengan nilai 1 hingga 5
b = np.array([5, 4, 3, 2, 1]) # Membuat array b dengan nilai 5 hingga 1

# Penjumalahan
add_result = a + b # Menambahkan elemen-elemen pada array a dan b

# Pengurangan
sub_result = a - b # Mengurangi elemen-elemen pada array a dan b

# Perkalian
mul_result = a * b # Mengalikan elemen-elemen pada array a dan b

# pembagian
div_result = a / b # Membagi elemen-elemen pada array a dan b

# Menampilkan hasil
print("Penjumlahan: ", add_result) # Menampilkan hasil penjumlahan
print("Pengurangan: ", sub_result) # Menampilkan hasil pengurangan
print("Perkalian: ", mul_result) # Menampilkan hasil perkalian
print("Pembagian: ", div_result) # Menampilkan hasil pembagian  
